  def self.effectiveness(atype, dtypes)
    atypen = get_typen(atype)
    dtypens = dtypes.map { |t| get_typen(t) }
    dtypens.map { |n| CHART[atypen][n] }.reduce(:*)
  end
  
  def self.get_typen(typename)
    result = TYPES.index(typename)
    raise "#{typename} is not a type" if !result
    result
  end
